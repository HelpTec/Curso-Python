
"""
repetitiva_mientras_1.py
script en python que muestre los numeros del 0 al 13 utilizando la
estructura repetitiva "mientras"
"""
print("es un programa que muestra los numeros del 0 al 13")
#contador
numero = 0

while numero <= 13:
    print(f"contador: {numero}")
    numero = numero + 1
else:
    print("fin del script")
